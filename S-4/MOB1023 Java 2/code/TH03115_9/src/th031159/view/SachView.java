    private void btnGhiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGhiMouseClicked
        // TODO add your handling code here:
//        ss.ghi();
        String ten = txtTen.getText().trim();
        String theLoai = cboTheLoai.getSelectedItem().toString();
        Integer giaSach = Integer.valueOf(txtGiaSach.getText().trim());
        
        try {
            if (ten.isEmpty() || theLoai.equals("Select") || txtGiaSach.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Vui lòng điền đẩy đủ thông tin!");
            } else if (giaSach < 50 || giaSach.toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Giá sách phải là số và lớn hơn 50!");
            } else {
                Sach s = new Sach(ten, theLoai, giaSach);
                
                int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    String kq = ss.add(s);
                    JOptionPane.showMessageDialog(this, kq);
                }
                
                loadData(ss.getList());
                ss.ghi();
            }
        } catch (NumberFormatException e) {
            System.out.println("Giá sách phải là số hợp lệ!");
        } catch (Exception e) {
            System.out.println("Có lỗi xảy ra " + e.getMessage());
        }
    }//GEN-LAST:event_btnGhiMouseClicked

