    void loadData(ArrayList<Sach> sList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblSach.getModel();
        tableModel.setRowCount(0);
        
        int stt = 1;
        for (Sach sach : sList) {
            tableModel.addRow(new Object[] {
                stt++,
                sach.getTen(),
                sach.getTheLoai(),
                sach.getGiaSach(),
            });
        }
    }

