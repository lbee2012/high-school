    void loadDataTable(ArrayList<KhachHang> list) {
        DefaultTableModel tableModel = (DefaultTableModel) tblKhachHang.getModel();
        tableModel.setRowCount(0);
        
        for (KhachHang kh : list) {
            tableModel.addRow(new Object[]{
                kh.getID(),
                kh.getHoTen(),
                kh.getDiaChi(),
                kh.getGioiTinh(),
                kh.getNamSinh()
            });
        }
    }
