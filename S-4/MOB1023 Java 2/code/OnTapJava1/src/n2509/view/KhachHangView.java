    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int row = tblKhachHang.getSelectedRow();
        KhachHang kh = qlkh.getList().get(row);
        
        String ID = txtID.getText();
        String hoTen = txtHoTen.getText();
        String diaChi = txtDiaChi.getText();
        String gioiTinh;
        
        if(rdNam.isSelected()) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nu";
        }
        
        Integer namSinh = Integer.parseInt(cboNamSinh.getSelectedItem().toString());
        
        KhachHang khachHang = new KhachHang(ID, hoTen, diaChi, gioiTinh, namSinh);
        
        String kq = qlkh.update(row, kh);
        JOptionPane.showMessageDialog(this, kq);
        
        loadDataTable(qlkh.getList());
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        // TODO add your handling code here:
        int row = tblKhachHang.getSelectedRow();
        KhachHang kh = qlkh.getList().get(row);
        
        txtID.setText(kh.getID());
        txtHoTen.setText(kh.getHoTen());
        txtDiaChi.setText(kh.getDiaChi());
        
        if (kh.getGioiTinh().equals("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        
        cboNamSinh.setSelectedItem(kh.getNamSinh().toString());
    }//GEN-LAST:event_tblKhachHangMouseClicked

