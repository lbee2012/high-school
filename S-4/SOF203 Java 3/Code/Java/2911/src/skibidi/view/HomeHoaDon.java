    private void tblHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoaDonMouseClicked
        // TODO add your handling code here:
        int row = tblHoaDon.getSelectedRow();
        
        Integer ma = (Integer) tblHoaDon.getValueAt(row, 0);
        HoaDon hd = repositoryHoaDon.details(ma);
        
        txtMaHD.setText(String.valueOf(hd.getMaHoaDon()));
        txtNgayLap.setText(String.valueOf(hd.getNgayLap()));
        txtMaKH.setText(String.valueOf(hd.getMaKhachHang()));
        txtTongTien.setText(String.valueOf(hd.getTongTien()));
        if (hd.getTrangThai().equals("Chua thanh toan")) {
            rdChuaTT.setSelected(true);
        } else {
            rdDaTT.setSelected(true);
        }
        txtGhiChu.setText(String.valueOf(hd.getGhiChu()));
    }//GEN-LAST:event_tblHoaDonMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        Integer maHD = Integer.valueOf(txtMaHD.getText());
        Date ngayLap = Date.valueOf(txtNgayLap.getText());
        Integer maKH = Integer.valueOf(txtMaKH.getText());
        Double tongTien = Double.valueOf(txtMaHD.getText());
        String trangThai;
        if (rdChuaTT.isSelected()) {
            trangThai = "Chua thanh toan";
        } else {
            trangThai = "Da thanh toan";
        }
        String ghiChu = txtGhiChu.getText();
        
        HoaDon hd = new HoaDon(maHD, ngayLap, maKH, tongTien, trangThai, ghiChu);
        Boolean kq = repositoryHoaDon.add(hd);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them thanh cong hoa don!");
            loadData(repositoryHoaDon.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them that bai hoa don!");
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        Integer maHD = Integer.valueOf(txtMaHD.getText());
        Date ngayLap = Date.valueOf(txtNgayLap.getText());
        Integer maKH = Integer.valueOf(txtMaKH.getText());
        Double tongTien = Double.valueOf(txtMaHD.getText());
        String trangThai;
        if (rdChuaTT.isSelected()) {
            trangThai = "Chua thanh toan";
        } else {
            trangThai = "Da thanh toan";
        }
        String ghiChu = txtGhiChu.getText();
        
        HoaDon hd = new HoaDon(maHD, ngayLap, maKH, tongTien, trangThai, ghiChu);
        Boolean kq = repositoryHoaDon.update(hd);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Cap nhat thanh cong hoa don!");
            loadData(repositoryHoaDon.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Cap nhat that bai hoa don!");
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

