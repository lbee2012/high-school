    void loadData(ArrayList<HocSinh> hsList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblHocSinh.getModel();
        tableModel.setRowCount(0);
        
        for (HocSinh hs : hsList) {
            tableModel.addRow(new Object[] {
                hs.getMaHocSinh(),
                hs.getHoTen(),
                hs.getGioiTinh() ? "Nam" : "Nu",
                hs.getLop(),
            });
        }
    }

