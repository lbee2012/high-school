    private void tblHocSinhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHocSinhMouseClicked
        // TODO add your handling code here:
        int row = tblHocSinh.getSelectedRow();
        
        String ma = (String) tblHocSinh.getValueAt(row, 0);
        HocSinh hs = repositoryHS.details(ma);
        
        txtMaHocSinh.setText(String.valueOf(hs.getMaHocSinh()));
        txtHoTen.setText(String.valueOf(hs.getHoTen()));
        if (hs.gioiTinh == true) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        txtLop.setText(String.valueOf(hs.getLop()));
    }//GEN-LAST:event_tblHocSinhMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        String maHocSinh = txtMaHocSinh.getText().trim();
        String hoTen = txtHoTen.getText().trim();
        Boolean gioiTinh;
        if (rdNam.isSelected()) {
            gioiTinh = true;
        } else {
            gioiTinh = false;
        }
        String lop = txtLop.getText().trim();
        
        HocSinh hs = new HocSinh(maHocSinh, hoTen, gioiTinh, lop);
        
        Boolean kq = repositoryHS.add(hs);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them mot hoc sinh thanh cong!");
            loadData(repositoryHS.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them mot hoc sinh that bai!");
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        // TODO add your handling code here:
        String ma = txtMaHocSinh.getText().trim();
        
        Boolean kq = repositoryHS.remove(ma);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Xoa mot hoc sinh thanh cong!");
            loadData(repositoryHS.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Xoa mot hoc sinh that bai!");
        }
    }//GEN-LAST:event_btnRemoveMouseClicked

    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // TODO add your handling code here:
        String ten = txtHoTen.getText().trim();
        
        ArrayList<HocSinh> kq = repositoryHS.search(ten);
        
        if (kq.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khong tim duoc hoc sinh nao co ten: " + ten);
        } else {
            JOptionPane.showMessageDialog(this, "Tim duoc nhung hoc sinh sau co ten: " + ten);
            loadData(kq);
        }
        
    }//GEN-LAST:event_btnSearchMouseClicked

