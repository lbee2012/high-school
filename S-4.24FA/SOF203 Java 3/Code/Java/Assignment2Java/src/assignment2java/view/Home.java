    void loadNguoi(ArrayList<Nguoi> nList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblNguoi.getModel();
        tableModel.setRowCount(0);
        
        for (Nguoi n : nList) {
            tableModel.addRow(new Object[] {
                n.getID(),
                n.getHoTen(),
                n.getGioiTinh(),
            });
        }
    }
    
    void loadSach(ArrayList<Sach> sList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblSach.getModel();
        tableModel.setRowCount(0);
        
        for (Sach s : sList) {
            tableModel.addRow(new Object[] {
                s.getID(),
                s.getTenSach(),
                s.getGiaBan(),
                s.getLoai(),
            });
        }
    }
    
    void loadComboBoxLoai(ArrayList<Sach> itemLoai) {
        DefaultComboBoxModel<String> comboBoxModel = (DefaultComboBoxModel<String>) cboLoai.getModel();
        comboBoxModel.removeAllElements();

        for (Sach s : itemLoai) {
            comboBoxModel.addElement(s.getLoai());
        }
    }


