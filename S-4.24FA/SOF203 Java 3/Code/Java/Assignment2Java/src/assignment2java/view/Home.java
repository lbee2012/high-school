    private void tblNguoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNguoiMouseClicked
        // TODO add your handling code here:
        int row = tblNguoi.getSelectedRow();
        
        Integer ID = (Integer) tblNguoi.getValueAt(row, 0);
        Nguoi n = repositoryNguoi.details(ID);
        
        txtIDNguoi.setText(String.valueOf(n.getID()));
        txtHoTen.setText(String.valueOf(n.getHoTen()));
        if (n.getGioiTinh().equals("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
    }//GEN-LAST:event_tblNguoiMouseClicked

    private void btnAddNguoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddNguoiMouseClicked
        // TODO add your handling code here:
        Integer ID = Integer.valueOf(txtIDNguoi.getText().trim());
        String hoTen = txtHoTen.getText().trim();
        String gioiTinh;
        if (rdNam.isSelected()) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nu";
        }
        
        Nguoi n = new Nguoi(ID, hoTen, gioiTinh);
        Boolean kq = repositoryNguoi.add(n);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            loadNguoi(repositoryNguoi.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them that bai");
        }
    }//GEN-LAST:event_btnAddNguoiMouseClicked

