    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        // TODO add your handling code here:
        int row = tblSinhVien.getSelectedRow();
        SinhVien sv = qlsv.getList().get(row);
        
        txtID.setText(sv.getID().toString());
        txtTen.setText(sv.getTen().toString());
        txtDiaChi.setText(sv.getDiaChi().toString());
        
        if (sv.getGioiTinh().equals("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        
        cboLop.setSelectedItem(sv.getLop().toString());
    }//GEN-LAST:event_tblSinhVienMouseClicked

