    void loadData(ArrayList<SinhVien> svList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblSinhVien.getModel();
        tableModel.setRowCount(0);
        
        for (SinhVien sinhVien : svList) {
            tableModel.addRow(new Object[]{
                sinhVien.getID(),
                sinhVien.getTen(),
                sinhVien.getDiaChi(),
                sinhVien.getGioiTinh(),
                sinhVien.getLop()
            });
        }
    }

